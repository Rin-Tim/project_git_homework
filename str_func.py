def str_func_upper():
    """функция возвращения строки со всеми заглавными буквами"""
    us_input = input("Впиши - ").upper()
    return us_input


print(str_func_upper())


def str_func_title():
    """функция делает заглавными первые буквы каждого слова в строке"""
    us_input = input("Впиши - ").title()
    return us_input


print(str_func_title())
